//program to display the pattern
public class pattern5
{
    public static void main()
    {
        System.out.println();
        for(int i=5;i>=3;i--)
        {
            for(int j=5;j>=3;j--)
            {
                System.out.print(i+" ");
            }
            System.out.println();
        }
    }
}
