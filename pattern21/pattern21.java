//program to display the pattern
public class pattern21
{
    public static void main()
    {
        System.out.println();
        for(int i=6;i>=1;i--)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print(i+" ");
            }
            System.out.println();
        }
    }
}
